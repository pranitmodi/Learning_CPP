#include <bits/stdc++.h>
using namespace std;
int main()
{
    int a = 23;
    double b = 2334.344;
    bool c = true;

    string str = "My name is C++";

    cout << "INT: " << sizeof(a) << endl;
    cout << "DOUBLE: " << sizeof(b) << endl;
    cout << "BOOLEAN: " << sizeof(c) << endl;
    cout << "STRING: " << sizeof(str) << endl;
}